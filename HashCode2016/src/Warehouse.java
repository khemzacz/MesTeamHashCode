
public class Warehouse {

Warehouse(int r, int c, int ID){
	setr(r);
	setc(c);
	setID(ID);
}


	
public int getr() {
	return r;
}
public int getc() {
	return c;
}
public int getID(){
	return ID;
}
public void setr(int r) {
	this.r=r;
}
public void setc(int c) {
	this.c=c;
}
public void setID(int id){
	this.ID=id;
}



private int ID;	

private int r;
private int c;
}
